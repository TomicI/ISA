package com.dto;

public class RateDTO {

    private int avio;
    private int letoc;
    private int fil;
    private int voz;
    private int hot;
    private int sob;

    public RateDTO() {
    }


    public int getAvio() {
        return avio;
    }

    public void setAvio(int avio) {
        this.avio = avio;
    }

    public int getLetoc() {
        return letoc;
    }

    public void setLetoc(int letoc) {
        this.letoc = letoc;
    }

    public int getFil() {
        return fil;
    }

    public void setFil(int fil) {
        this.fil = fil;
    }

    public int getVoz() {
        return voz;
    }

    public void setVoz(int voz) {
        this.voz = voz;
    }

    public int getHot() {
        return hot;
    }

    public void setHot(int hot) {
        this.hot = hot;
    }

    public int getSob() {
        return sob;
    }

    public void setSob(int sob) {
        this.sob = sob;
    }
}
